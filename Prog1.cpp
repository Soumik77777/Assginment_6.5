#include<iostream>
using namespace std;
int main()
{
    //apple loop
    for(int a=1;a<=100;a++)
    {
        //mango loop
        for(int m=1;m<=100;m++)
        {
            //banana
            int b=100-(a+b);
            if((a+3*m+0.5*b)==100)
            {
                cout<<"Mangoes"<<m<<"Bananas"<<b<<"Apples"<<a;
            }
        }
    }
}
            
    
